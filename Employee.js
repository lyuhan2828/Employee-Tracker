class Employee {
    constructor() {
        this.firstName;
        this.lastName;
        this.roleId;
        this.managerId
    }
    getFistName() {
        return this.firstName;
    }
    getLastName() {
        return this.lastName;
    }
    getRoleId() {
        return this.roleId;
    }
    getManagerId() {
        return this.managerId;
    }
}

export default Employee;